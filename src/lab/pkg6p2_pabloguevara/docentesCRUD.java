    FileOutputStream fichero3;

    public void guardarDocentes() {
        fichero3 = null;//CREO VARIABLE FICHERO
        try {

            fichero3 = new FileOutputStream("docentes.txt");//Creamos el archivo donde se guarda
            ObjectOutputStream tuberia1 = new ObjectOutputStream(fichero3);//asignamos los valores al objeto
            tuberia1.writeObject(listaDocentes);//Guardamos el objeto
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        } finally {
            try {
                fichero3.close();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }

    public void cargarDocentes() {
        FileInputStream ficheroEntrada1 = null;
        File file1 = new File("docentes.txt");
        try {

            if (file1.exists()) {
                ficheroEntrada1 = new FileInputStream("docentes.txt");
                ObjectInputStream tuberiaEntrada1 = new ObjectInputStream(ficheroEntrada1);
                listaDocentes = (Lista) tuberiaEntrada1.readObject();
            }

        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    }

