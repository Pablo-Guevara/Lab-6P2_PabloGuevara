    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila = jTable1.getRowCount();
        if (fila != 0) {
            for (int i = fila - 1; i >= 0; i--) {
                tm.removeRow(i);
            }
        }
        listaDocentes.asignarNodo();

        for (int i = 0; i < listaDocentes.getTamDocente(); i++) {

            tm.addRow(new Object[]{listaDocentes.nombreRetornaDocente(), listaDocentes.ApellidoRetornaDocente(), listaDocentes.contraseñaRetornaDocente()});
            listaDocentes.siguienteEnLista();
        }
        jTable1.setRowHeight(100);
        jTable1.setModel(tm);
    }//GEN-LAST:event_jButton2ActionPerformed

