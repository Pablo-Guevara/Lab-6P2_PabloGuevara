    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int fila = jTable1.getRowCount();
        if (fila != 0) {
            for (int i = fila - 1; i >= 0; i--) {
                tm.removeRow(i);
            }
        }
        listaProyecto.asignarNodo();
        for (int i = 0; i < listaProyecto.getTamProyecto(); i++) {

            tm.addRow(new Object[]{listaProyecto.tituloRetornaProyecto(), listaProyecto.descripcionRetornaProyecto()});
            listaProyecto.siguienteEnLista();
        }
        jTable1.setRowHeight(100);
        jTable1.setModel(tm);
    }//GEN-LAST:event_jButton2ActionPerformed
    FileOutputStream fichero4;

    public void guardarProyecto() {
        fichero4 = null;//CREO VARIABLE FICHERO
        try {

            fichero4 = new FileOutputStream("proyecto.txt");//Creamos el archivo donde se guarda
            ObjectOutputStream tuberia1 = new ObjectOutputStream(fichero4);//asignamos los valores al objeto
            tuberia1.writeObject(listaProyecto);//Guardamos el objeto
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        } finally {
            try {
                fichero4.close();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }

    public void cargarProyectos() {
        FileInputStream ficheroEntrada1 = null;
        File file1 = new File("proyecto.txt");
        try {

            if (file1.exists()) {
                ficheroEntrada1 = new FileInputStream("proyecto.txt");
                ObjectInputStream tuberiaEntrada1 = new ObjectInputStream(ficheroEntrada1);
                listaProyecto = (Lista) tuberiaEntrada1.readObject();
            }

        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    }

